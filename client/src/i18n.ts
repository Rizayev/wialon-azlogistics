const DICTS: Record<Lang, Dict> = { az, ru, en };

export function translate(lang: Lang, key: string, params?: Record<string, string | number>): string {
  let s = DICTS[lang]?.[key] ?? DICTS.ru[key] ?? key;
  if (params) for (const k of Object.keys(params)) s = s.replace(`{${k}}`, String(params[k]));
  return s;
}

/** Seconds -> localized "X h Y min" / "Y min Z sec" / "Z sec". */
export function formatDuration(sec: number, lang: Lang): string {
  sec = Math.max(0, Math.round(sec));
  const h = Math.floor(sec / 3600);
  const m = Math.floor((sec % 3600) / 60);
  const s = sec % 60;
  const H = translate(lang, 'dur.h');
  const M = translate(lang, 'dur.min');
  const S = translate(lang, 'dur.sec');
  if (h > 0) return m > 0 ? `${h} ${H} ${m} ${M}` : `${h} ${H}`;
  if (m > 0) return s > 0 ? `${m} ${M} ${s} ${S}` : `${m} ${M}`;
  return `${s} ${S}`;
}
